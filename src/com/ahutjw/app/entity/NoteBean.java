package com.ahutjw.app.entity;

public class NoteBean {

	private String ID;
	private String courseName;
	private String stuNum;
	private String teaNum;
	private String Note;
	private String noteDate;
	private String noteCheck;
	private String Reply;
	private String replyDate;
	private String replyCheck;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getStuNum() {
		return stuNum;
	}
	public void setStuNum(String stuNum) {
		this.stuNum = stuNum;
	}
	public String getTeaNum() {
		return teaNum;
	}
	public void setTeaNum(String teaNum) {
		this.teaNum = teaNum;
	}
	public String getNote() {
		return Note;
	}
	public void setNote(String note) {
		Note = note;
	}
	public String getNoteDate() {
		return noteDate;
	}
	public void setNoteDate(String noteDate) {
		this.noteDate = noteDate;
	}
	public String getNoteCheck() {
		return noteCheck;
	}
	public void setNoteCheck(String noteCheck) {
		this.noteCheck = noteCheck;
	}
	public String getReply() {
		return Reply;
	}
	public void setReply(String reply) {
		Reply = reply;
	}
	public String getReplyDate() {
		return replyDate;
	}
	public void setReplyDate(String replyDate) {
		this.replyDate = replyDate;
	}
	public String getReplyCheck() {
		return replyCheck;
	}
	public void setReplyCheck(String replyCheck) {
		this.replyCheck = replyCheck;
	}

	

}
