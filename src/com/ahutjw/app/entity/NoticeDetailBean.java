package com.ahutjw.app.entity;

public class NoticeDetailBean {
	private String title;
	private String dtmPublish;
	private String seeNumber;
	private String content;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDtmPublish() {
		return dtmPublish;
	}

	public void setDtmPublish(String dtmPublish) {
		this.dtmPublish = dtmPublish;
	}

	public String getSeeNumber() {
		return seeNumber;
	}

	public void setSeeNumber(String seeNumber) {
		this.seeNumber = seeNumber;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
