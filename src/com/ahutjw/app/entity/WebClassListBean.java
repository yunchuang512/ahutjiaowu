package com.ahutjw.app.entity;

public class WebClassListBean {
	private String lessonCode;
	private String lessonId;
	private String lessonName;
	private String lessonProperty;
	private String lessonScore;
	private String whetherPicked;
	private String leftNum;

	public String getLessonCode() {
		return lessonCode;
	}

	public void setLessonCode(String lessonCode) {
		this.lessonCode = lessonCode;
	}

	public String getLessonId() {
		return lessonId;
	}

	public void setLessonId(String lessonId) {
		this.lessonId = lessonId;
	}

	public String getLessonName() {
		return lessonName;
	}

	public void setLessonName(String lessonName) {
		this.lessonName = lessonName;
	}

	public String getLessonProperty() {
		return lessonProperty;
	}

	public void setLessonProperty(String lessonProperty) {
		this.lessonProperty = lessonProperty;
	}

	public String getLessonScore() {
		return lessonScore;
	}

	public void setLessonScore(String lessonScore) {
		this.lessonScore = lessonScore;
	}

	public String getWhetherPicked() {
		return whetherPicked;
	}

	public void setWhetherPicked(String whetherPicked) {
		this.whetherPicked = whetherPicked;
	}

	public String getLeftNum() {
		return leftNum;
	}

	public void setLeftNum(String leftNum) {
		this.leftNum = leftNum;
	}

}
